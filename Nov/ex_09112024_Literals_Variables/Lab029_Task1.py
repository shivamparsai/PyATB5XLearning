# Take 3 user i/p and perform sum, sub, mul, div
value1 = int(input("Enter 1st value\n"))
value2 = int(input("Enter 2nd value\n"))
value3 = int(input("Enter 3rd value\n"))

print("Addition is ", value1 + value2 + value3)
print("Subtraction is ", value1 - value2 - value3)
print("Multiplication is ", value1 * value2 * value3)
print("Division is ", value1 / value2 / value3)
