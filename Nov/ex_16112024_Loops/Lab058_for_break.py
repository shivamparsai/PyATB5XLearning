for i in range(0, 10):
    print(i)
    if i == 5:
        break


# Break KW - break will exit from the loop.

# i | condition | o/p
# 0 | 0 == 5 -> False | o/p = 0
# 1 | 1 == 5 -> False | o/p = 1
# 2 | 2 == 5 -> False | o/p = 2
# 3 | 3 == 5 -> False | o/p = 3
# 4 | 4 == 5 -> False | o/p = 4
# 5 | 5 == 5 -> True | o/p = 5, print 5 and exit the loop