for num in range(10):
    if num % 2 == 0:
        continue
    else:
        print(num)

# Continue - continue statement skips the current iteration of the loop and moves to the next iteration


# i | condition | o/p
# 0 | 0 % 2 == 0 -> True | o/p - continue, do nothing
# 1 | 1 % 2 == 0 -> False | o/p - 1

